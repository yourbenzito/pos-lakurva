    /**
     * Modelo IVA 19% + Comisión 40% (Chile/Notebook style)
     * total = final_price; 
     * price_with_tax = total / 1.40; 
     * base = Math.round(price_with_tax / 1.19); 
     * tax = price_with_tax - base; 
     * commission = total - price_with_tax.
     * @param {number} total 
     */
    /**
     * Modelo IVA 19% + Comisión 40% (Chile/Notebook style)
     * total = final_price; 
     * price_with_tax = total / 1.40; 
     * base = Math.round(price_with_tax / 1.19); 
     * tax = price_with_tax - base; 
     * commission = total - price_with_tax.
     * @param {number} total 
     * @param {string} documentType - 'boleta' o 'sin_boleta'
     */
    static computeFiscalFromTotal(total, documentType = 'boleta') {
        const TAX_RATE = 0.19;
        const COMMISSION_RATE = 0.40;

        // Si no es boleta, no hay IVA para este reporte
        if (documentType !== 'boleta') {
            return {
                base_amount: total,
                tax_amount: 0,
                commission_amount: Math.round(total - (total / (1 + COMMISSION_RATE))),
                tax_rate: 0,
                commission_rate: COMMISSION_RATE,
                price_with_tax: total
            };
        }

        // Formula Legal Chile (Boletas)
        // IVA = Total - Redondeo(Total / 1.19)
        const baseAmount = Math.round(total / (1 + TAX_RATE));
        const taxAmount = total - baseAmount;

        // Comisión interna (Profit sugerido 40% margin)
        const commissionAmount = Math.round(total - (total / (1 + COMMISSION_RATE)));

        return {
            base_amount: baseAmount, // Neto legal
            tax_amount: taxAmount,   // IVA Débito legal (19%)
            commission_amount: commissionAmount, // Profit interno
            tax_rate: TAX_RATE,
            commission_rate: COMMISSION_RATE,
            price_with_tax: total // Legalmente todo el monto es la venta afecta
        };
    }

