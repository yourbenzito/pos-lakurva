    /**
     * Register payment (IMMUTABLE - creates new object instead of mutating)
     * @param {number} saleId - Sale ID
     * @param {number} amount - Payment amount
     * @returns {Promise<Object>} - Updated sale
     */
    static async registerPayment(saleId, amount) {
        const sale = await this.getById(saleId);
        if (!sale) throw new Error('Venta no encontrada');

        // Verify we're updating the correct sale
        if (sale.id !== saleId) {
            throw new Error('Error: ID de venta no coincide');
        }

        const paymentAmount = parseFloat(amount) || 0;
        if (paymentAmount <= 0) {
            throw new Error('El monto del pago debe ser mayor a 0');
        }

        const originalPaidAmount = parseFloat(sale.paidAmount) || 0;
        const originalTotal = parseFloat(sale.total) || 0;
        const originalRemaining = originalTotal - originalPaidAmount;

        // Verify we're not trying to pay more than what's remaining
        if (paymentAmount > originalRemaining) {
            console.warn('Payment amount exceeds remaining debt. Adjusting...', {
                saleId,
                paymentAmount,
                originalRemaining
            });
            const adjustedAmount = Math.min(paymentAmount, originalRemaining);
            if (adjustedAmount <= 0) {
                throw new Error('La venta ya está completamente pagada');
            }
            // Continue with adjusted amount
        }

        // Calculate new paid amount (IMMUTABLE - create new values)
        const newPaidAmount = Math.min(originalPaidAmount + paymentAmount, originalTotal);
        const isFullyPaid = newPaidAmount >= originalTotal;
        const hasPartialPayment = newPaidAmount > 0 && newPaidAmount < originalTotal;

        // Determine status: paid (completed), partial, or pending
        let newStatus = sale.status;
        if (isFullyPaid) {
            newStatus = 'completed'; // Fully paid
        } else if (hasPartialPayment) {
            newStatus = 'partial'; // Partially paid
        } else {
            newStatus = 'pending'; // No payment yet
        }

        // Create updated sale object (IMMUTABLE)
        const updated = {
            ...sale,
            paidAmount: isFullyPaid ? originalTotal : newPaidAmount,
            status: newStatus,
            updatedAt: new Date().toISOString() // B6
        };

        // Replace sale using repository
        await this._repository.replace(updated);

        // Verify the update worked correctly
        const verifiedSale = await this.getById(saleId);
        if (!verifiedSale) {
            throw new Error('Error: No se pudo verificar la actualización de la venta');
        }

        const expectedPaidAmount = Math.min(originalPaidAmount + paymentAmount, originalTotal);
        if (Math.abs(verifiedSale.paidAmount - expectedPaidAmount) > 0.01) {
            console.error('Payment update mismatch!', {
                saleId,
                originalPaidAmount,
                paymentAmount,
                expectedPaidAmount,
                actualPaidAmount: verifiedSale.paidAmount,
                originalTotal
            });
            throw new Error(`Error: El monto pagado no se actualizó correctamente. Esperado: ${expectedPaidAmount}, Actual: ${verifiedSale.paidAmount}`);
        }

        return verifiedSale;
    }

